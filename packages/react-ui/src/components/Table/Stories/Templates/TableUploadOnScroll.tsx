const LastRowWrapper = ({
  containerRef,
  onVisible,
  rowNode,
}: {
  containerRef: React.RefObject<HTMLElement>;
  onVisible: () => void;
  rowNode: React.ReactNode;
}) => {
  const [visible, setVisible] = React.useState<boolean>(false);
  const ref = React.useRef<HTMLDivElement>(null);

  const handleIntersection = (entries: IntersectionObserverEntry[]) => {
    if (entries[0].isIntersecting && !visible) {
      setVisible(true);
      onVisible?.();
    }

    if (!entries[0].isIntersecting && visible) {
      setVisible(false);
    }
  };

  React.useEffect(() => {
    const observer = new IntersectionObserver(handleIntersection, {
      root: containerRef?.current,
      threshold: [0, 1.0],
    });

    if (containerRef?.current && ref.current) {
      observer.observe(ref.current);
    }

    return () => observer.disconnect();
  }, [containerRef, visible]);

  return <div ref={ref}>{rowNode}</div>;
};

export const UploadOnScrollTemplate = (props: TableProps) => {
  const [cols, setCols] = React.useState(columnList);
  const [rowsAmount, setRowsAmount] = React.useState(10);

  const tableRef = React.useRef<HTMLDivElement>(null);
  const rows = React.useMemo(() => {
    const array = Array.from({ length: rowsAmount }, (v, k) => {
      return `${k}0000`;
    }).map((item, index) => ({
      id: item,
      transfer_type: 'МНО',
      transfer_amount: item,
      currency: 'RUB',
    }));
    return array;
  }, [rowsAmount]);

  const handleResize = ({ name, width }: { name: string; width: string }) => {
    const newCols = cols.map((col) => (col.name === name ? { ...col, width } : col));
    setCols(newCols);
  };

  const uploadRows = () => {
    setRowsAmount((amount) => amount + 10);
  };

  const renderRowWrapper = (row: TableRow, index: number, rowNode: React.ReactNode) =>
    index === rowsAmount - 1 ? (
      <LastRowWrapper containerRef={tableRef} onVisible={uploadRows} rowNode={rowNode} />
    ) : (
      rowNode
    );

  return (
    <Table
      {...props}
      ref={tableRef}
      rowList={rows}
      columnList={cols}
      onColumnResize={handleResize}
      renderRowWrapper={renderRowWrapper}
    />
  );
};
